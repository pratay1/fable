    static void DrawMenuStoneDetailPass41(Rectangle region, MenuCastlePalette palette, float time)
    {
        // Draw hairline fracture pass 41
        int count = 15;
        for (int i = 0; i < count; i++)
        {
            int seed = 5617 + i * 29;
            float px = region.X + Hash(seed) * region.Width;
            float py = region.Y + Hash(seed + 3) * region.Height;
            float n = Hash(seed + 7);
            float n2 = Hash(seed + 11);
            Vector2 a = new Vector2(px, py);
            Vector2 b = new Vector2(px + (n - 0.5f) * 18f, py + 6f + n2 * 22f);
            Raylib.DrawLineEx(a, b, 0.6f + n * 0.4f, WithAlpha(palette.StoneDeep, 0.12f + n2 * 0.18f));
            if (n > 0.55f)
                Raylib.DrawLineEx(new Vector2(b.X + 2f, b.Y), new Vector2(b.X + (n2 - 0.5f) * 10f, b.Y + 8f), 0.5f, WithAlpha(palette.Mortar, 0.15f));
        }
    }

